FRESULT ok(FRESULT *fr) {// This is the function to check what needs to be done. 

    if(!exists_check.path_exists)
    {
         *fr = FR_NO_PATH;
         return(*fr);
    }

    if(!exists_check.folder_exists)
    {
        *fr = FR_NO_FOLDER;
        return(*fr);
    }

    if(!exists_check.file_exists)
    {
        *fr = FR_NO_FILE;
        return(*fr);
    }

}

